    private void botonIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIniciarSesionActionPerformed
        USER = cajaUsuario.getText();
        PASS = cajaContrasena.getText();
        final String sql = "SELECT * FROM \"pg_authid\" WHERE rolcanlogin = true;";
        ResultSet rs;
        
        //realizamos la consulta de usuarios
        try {
            db = Database.getDatabase(USER, PASS);
            if (db != null) {
                // Enviar consulta a la base de datos
                if (usuarios.isEmpty()) {
                    rs = db.query(sql);
                    while (rs.next()) {
                        //2 = nombre    3 = rolsuper    4 = rolinherit
                        //5 = rolcreaterole     6 = rolcreatedb     8 rolreplication

                        //obtenemos los datos de la base de datos
                        String nombre = rs.getString(2);

                        Usuarios usuario = new Usuarios(nombre, rs.getBoolean(3), rs.getBoolean(4), rs.getBoolean(5), rs.getBoolean(6), rs.getBoolean(8));
                        // Agregar nombre del proveedor al combo
                        usuarios.put(nombre, usuario);
                        //System.out.println(label);

                    }
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        if (db != null) {
            //si puede crear roles
            if (usuarios.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Usted no tiene permitido hacer respaldos");
            } else if (usuarios.get(USER).isRolSuper() == true || usuarios.get(USER).isRolReplication()) {
                ventanaPrincipal vp = new ventanaPrincipal(db,USER,PASS);
                vp.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usted no tiene permitido hacer respaldos");
            }
        }
    }//GEN-LAST:event_botonIniciarSesionActionPerformed

